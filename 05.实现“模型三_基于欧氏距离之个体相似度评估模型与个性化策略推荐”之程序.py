# 阶段 4：读取数据、计算 POMP 分数与标签分组
# ==========================================
def process_pomp_and_grouping(csv_file):
    if not os.path.exists(csv_file):
        print(f"❌ 找不到 {csv_file}！请确认你已经上传文件。")
        return None

    df = pd.read_csv(csv_file)

    if COL_STUDENT_SCORE not in df.columns or COL_SELFIE_SCORE not in df.columns:
        print(f"❌ 错误：在 CSV 中找不到名为 '{COL_STUDENT_SCORE}' 的列。")
        return None

    df_clean = df.dropna(subset=[COL_STUDENT_SCORE, COL_SELFIE_SCORE]).copy()
    print(f"\n🚀 [阶段四] 成功读取到 {len(df_clean)} 位同学的有效分数。")

    df_clean['POMP_Score'] = (df_clean[COL_SELFIE_SCORE] - df_clean[COL_STUDENT_SCORE]) / (10 - df_clean[COL_STUDENT_SCORE] + 1e-5)
    median_pomp = df_clean['POMP_Score'].median()
    df_clean['Group'] = (df_clean['POMP_Score'] >= median_pomp).astype(int)

    df_clean['群组标签'] = df_clean['Group'].map({
        1: '高成效策略群组 (赢家)',
        0: '次佳策略群组 (一般)'
    })

    output_csv = "Final_Student_Dataset_Analyzed.csv"
    df_clean.to_csv(output_csv, index=False, encoding='utf_8_sig')

    print(f"✅ POMP 中位数为: {median_pomp:.3f}")
    print(f"🏆 赢家池人数: {sum(df_clean['Group']==1)} 人 | 📉 次佳组人数: {sum(df_clean['Group']==0)} 人\n")

    return df_clean

