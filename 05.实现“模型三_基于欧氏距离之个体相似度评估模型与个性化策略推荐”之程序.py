# 阶段 6：个性化策略推荐系统 (欧氏距离与反推建议)
# ==========================================
def run_personalized_recommendation(df):
    print("🤖 [阶段六] 启动：个性化修图推荐系统 (K=1 匹配与物理反推)")

    base_metrics = ["fWHR", "Yaw", "Pitch", "GRI", "LCI", "AI"]

    # 取 ID照原生特征作为距离比对标准
    feature_cols = [f"{m}_ID(b.jpg)" for m in base_metrics]
    # 取对数变形率作为策略提取标准
    rfvr_cols = [f"LogRatio_{m} (对数变形率)" for m in base_metrics]

    winner_pool = df[df['Group'] == 1].copy()
    suboptimal_group = df[df['Group'] == 0].copy()

    scaler = StandardScaler()
    scaler.fit(df[feature_cols])
    winners_scaled = scaler.transform(winner_pool[feature_cols])
    suboptimal_scaled = scaler.transform(suboptimal_group[feature_cols])

    recommendation_results = []
    print(f"✅ 正在为 {len(suboptimal_group)} 位次佳组同学寻找几何双胞胎...\n")

    for i, (idx, user_row) in enumerate(suboptimal_group.iterrows()):
        user_z = suboptimal_scaled[i].reshape(1, -1)

        distances = np.linalg.norm(winners_scaled - user_z, axis=1)
        best_match_idx = np.argmin(distances)
        min_dist = distances[best_match_idx]

        twin_id = winner_pool.index[best_match_idx]
        twin_rfvr = winner_pool.loc[twin_id, rfvr_cols]
        u_orig = user_row[feature_cols]

        # 轨道一：实体动作
        ta_yaw = u_orig['Yaw_ID(b.jpg)'] * math.exp(twin_rfvr["LogRatio_Yaw (对数变形率)"])
        ta_yaw_clamped = max(0, min(ta_yaw, 0.99))
        u_yaw_clamped = max(0, min(u_orig['Yaw_ID(b.jpg)'], 0.99))
        delta_yaw_deg = math.degrees(math.asin(ta_yaw_clamped) - math.asin(u_yaw_clamped))
        yaw_dir = "向侧边(左/右)转动" if delta_yaw_deg > 0 else "脸部向正中央转回"

        ta_pitch = u_orig['Pitch_ID(b.jpg)'] * math.exp(twin_rfvr["LogRatio_Pitch (对数变形率)"])
        delta_pitch_deg = math.degrees(math.atan(ta_pitch) - math.atan(u_orig['Pitch_ID(b.jpg)']))
        pitch_dir = "相机抬高(或收下巴)" if delta_pitch_deg > 0 else "相机降低(或抬下巴)"

        # 轨道二：数字后期
        p_fwhr = (math.exp(twin_rfvr["LogRatio_fWHR (对数变形率)"]) - 1) * 100
        p_lci = (math.exp(twin_rfvr["LogRatio_LCI (对数变形率)"]) - 1) * 100

        # 预期优化目标
        p_gri = (math.exp(twin_rfvr["LogRatio_GRI (对数变形率)"]) - 1) * 100
        p_ai = (math.exp(twin_rfvr["LogRatio_AI (对数变形率)"]) - 1) * 100

        # 取出学号
        subject_id = user_row['Student_ID'] if 'Student_ID' in user_row else user_row.name
        twin_student_id = winner_pool.loc[twin_id, 'Student_ID'] if 'Student_ID' in winner_pool.columns else twin_id

        recommendation_results.append({
            "Subject_ID": subject_id,
            "Twin_ID": twin_student_id,
            "Distance": round(min_dist, 4),
            "Suggest_Yaw_Deg": round(delta_yaw_deg, 2),
            "Suggest_Pitch_Deg": round(delta_pitch_deg, 2),
            "Suggest_fWHR_Slider": round(p_fwhr, 2),
            "Suggest_LCI_Slider": round(p_lci, 2)
        })

        # 【解除封印】：将全班次佳组同学的报告全部打印出来！
        print(f"👤 测试者学号: {subject_id} (最相似赢家学号: {twin_student_id}, 距离: {min_dist:.4f})")
        print(f"   [轨道一：实体摄影端建议]")
        print(f"   ▶ 水平转动：{yaw_dir}约 {abs(delta_yaw_deg):.2f} 度")
        print(f"   ▶ 俯仰微调：{pitch_dir}约 {abs(delta_pitch_deg):.2f} 度")
        print(f"   [轨道二：数字 APP 后期参数]")
        print(f"   ▶ 脸宽拉杆：{'向右拉宽' if p_fwhr > 0 else '向左缩窄'} {abs(p_fwhr):.2f}%")
        print(f"   ▶ 光影对比：{'增强打亮' if p_lci > 0 else '柔化降低'} {abs(p_lci):.2f}%")
        print(f"   [预期整体美学目标]")
        if p_gri < 0: print(f"   ▶ 黄金比例 (GRI) 误差减少：{abs(p_gri):.2f}% (五官比例趋于和谐)")
        else: print(f"   ▶ 黄金比例 (GRI) 误差增加：{p_gri:.2f}% (往个人特色化方向发展)")
        if p_ai < 0: print(f"   ▶ 不对称度 (AI) 改善降低：{abs(p_ai):.2f}% (通过角度投影掩盖，让脸型更对称)")
        else: print(f"   ▶ 不对称度 (AI) 反向增加：{p_ai:.2f}% (通过角度掩盖与修正))")
        print("-" * 65)

    rec_df = pd.DataFrame(recommendation_results)
    rec_df.to_csv("Personalized_Optimization_Strategies.csv", index=False, encoding='utf_8_sig')
    print(f"📁 全班完整推荐策略清单已存档：Personalized_Optimization_Strategies.csv")
    print("=" * 65)

