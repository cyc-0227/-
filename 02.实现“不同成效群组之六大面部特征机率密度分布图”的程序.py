# 阶段 5：高斯单纯贝氏模型 (绘制 6 宫格统计图)
# ==========================================
def plot_bayesian_gaussian(df):
    print("🧠 启动阶段五：构建高斯贝氏机率模型...")

    features = ["fWHR", "Yaw", "Pitch", "GRI", "LCI", "AI"]
    titles = ['fWHR (脸型宽高)', 'Yaw (水平转头)', 'Pitch (俯仰低头)', 'GRI (黄金比例)', 'LCI (光影对比)', 'AI (不对称度)']

    fig, axes = plt.subplots(2, 3, figsize=(18, 12))
    axes = axes.flatten()

    for i, metric in enumerate(features):
        ax = axes[i]
        col_name = f"LogRatio_{metric} (对数变形率)"

        # 提取两组的对数变形率数据
        data_high = df[df['Group'] == 1][col_name]
        data_low = df[df['Group'] == 0][col_name]

        # 计算期望值(mu)与标准偏差(std)
        mu1, std1 = data_high.mean(), max(data_high.std(), 1e-3)
        mu0, std0 = data_low.mean(), max(data_low.std(), 1e-3)

        # 设定 X 轴绘图范围
        x_min = min(data_high.min(), data_low.min())
        x_max = max(data_high.max(), data_low.max())
        margin = (x_max - x_min) * 0.2 if x_max != x_min else 0.1
        x_axis = np.linspace(x_min - margin, x_max + margin, 200)

        # 绘制高成效组 (红线)
        pdf_high = stats.norm.pdf(x_axis, mu1, std1)
        ax.plot(x_axis, pdf_high, color='#e74c3c', lw=3, label='高成效策略群组 (赢家)')
        ax.fill_between(x_axis, pdf_high, color='#e74c3c', alpha=0.2)

        # 绘制次佳策略组 (蓝虚线)
        pdf_low = stats.norm.pdf(x_axis, mu0, std0)
        ax.plot(x_axis, pdf_low, color='#3498db', lw=2, linestyle='--', label='次佳策略群组 (一般)')
        ax.fill_between(x_axis, pdf_low, color='#3498db', alpha=0.1)

        # 图表美化
        ax.set_title(f"【{titles[i]}】 策略分布 (机率密度)", fontsize=15, fontweight='bold', pad=10)
        ax.set_xlabel("对数变形率 (Log-Ratio) \n <0:缩小/减少 | =0:无变化 | >0:放大/增加", fontsize=10)
        ax.set_ylabel("机率密度 (PDF)", fontsize=10)

        # 标注 mu 数值框
        textstr = f"高成效 $\\mu$={mu1:.3f}\n次佳组 $\\mu$={mu0:.3f}"
        props = dict(boxstyle='round', facecolor='white', alpha=0.8, edgecolor='gray')
        ax.text(0.05, 0.95, textstr, transform=ax.transAxes, fontsize=11,
                verticalalignment='top', bbox=props)

        if i == 0:
            ax.legend(loc='lower left', fontsize=11)

    plt.suptitle("6D 高斯单纯贝氏模型：自拍修图策略成效之机率密度解析", fontsize=22, fontweight='bold', y=0.95)
    plt.tight_layout(rect=[0, 0, 1, 0.93])

    # 储存图表
    save_path = "Bayesian_6D_Gaussian_Comparison.png"
    plt.savefig(save_path, dpi=300)
    plt.close(fig)
    print(f"✅ [阶段五完成] 完美对比的 6 宫格统计图已生成：{save_path}")

# ==========================================
# 主程序执行区
# ==========================================
df_grouped = process_pomp_and_grouping("Final_Student_Dataset.csv")

if df_grouped is not None:
    plot_bayesian_gaussian(df_grouped)