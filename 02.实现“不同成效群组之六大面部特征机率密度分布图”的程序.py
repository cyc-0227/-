# 阶段 4：读取数据、计算 POMP 分数与卷标分组
# ==========================================
def process_pomp_and_grouping(csv_file="Final_Student_Dataset.csv"):
    if not os.path.exists(csv_file):
        print(f"❌ 找不到 {csv_file}！请确认你已经上传填好分数的 CSV 档案。")
        return None

    df = pd.read_csv(csv_file)

    # 检查是否有分数字段并过滤掉没有填分数的学生
    if '学生照分数(b)' not in df.columns or '个人照分数(a)' not in df.columns:
        print("❌ CSV 中找不到分数字段，请确认域名。")
        return None

    df_clean = df.dropna(subset=['学生照分数(b)', '个人照分数(a)']).copy()

    if len(df_clean) == 0:
        print("❌ 没有读取到任何有效的分数，请确认是否填入了数字。")
        return None

    print(f"🚀 开始处理阶段四：读取到 {len(df_clean)} 位同学的有效分数。")

    # 计算 POMP Score
    df_clean['POMP_Score'] = (df_clean['个人照分数(a)'] - df_clean['学生照分数(b)']) / (10 - df_clean['学生照分数(b)'] + 1e-5)

    # 计算中位数并分组
    median_pomp = df_clean['POMP_Score'].median()
    df_clean['Group'] = (df_clean['POMP_Score'] >= median_pomp).astype(int)

    # 【学术命名】赋予高质感的组名
    df_clean['群组标签'] = df_clean['Group'].map({
        1: '高成效策略群组 (赢家)',
        0: '次佳策略群组 (一般)'
    })

    # 储存带有分组结果的新 CSV
    output_csv = "Final_Student_Dataset_Grouped.csv"
    df_clean.to_csv(output_csv, index=False, encoding='utf_8_sig')

    print("\n✅ [阶段四完成] POMP 分数与群组切分完毕！")
    print(f"📊 POMP 中位数为: {median_pomp:.3f}")
    print(f"🏆 高成效策略群组人数: {sum(df_clean['Group']==1)} 人")
    print(f"📉 次佳策略群组人数: {sum(df_clean['Group']==0)} 人")
    print(f"📁 分组名单已另存为: {output_csv} (你可以打开来看谁在哪个群组)\n")

    return df_clean

